$(function() {
    $('[name="send_type"]').on('click',function() {
        if ($('#send_type_1').prop('checked')) {
            $('[method0]').addClass('active');
            $('[method1]').removeClass('active');
        }else{
            $('[method1]').addClass('active');
            $('[method0]').removeClass('active');
        }
    })
})

