$(function(){
    $('input:radio[name="type"]').on('click',function() {
        if($('#pri_type').prop('checked')){
            $('#require').show();
        }else{
            $('#require').hide();
        }
    });
});

$(document).ready(function() {
    if($('#nor_type').prop('checked')){
        $('#require').hide();
    }
});


