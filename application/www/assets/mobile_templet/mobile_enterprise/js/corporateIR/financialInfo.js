$('button').on('click', function () {
    if($(this).hasClass('tab--first')){
        $('.br__ir__detail--first').show();
        $('.br__ir__detail--second').hide();
    }else{
        $('.br__ir__detail--first').hide();
        $('.br__ir__detail--second').show();
    }
});
